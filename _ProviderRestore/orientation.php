            <div class="col-sm-12 about-us-text wow fadeInLeft">
                <h3>- L'orientation des élèves de 3ème</h3>
                <br>
                <span class="violet"><h4>Entretien individuel d'orientation</h4></span>
                <p>
                    <strong>Son but:</strong> aider l'élève à atteindre ses objectifs et à repérer au plus vite ses éventuelles difficultés.
                    L’entretien individuel d’orientation, qui se tient le plus tôt possible dans l'année scolaire, aide l'élève à faire le point sur son parcours de formation et à examiner quelles études sont possibles à envisager.
                </p>
               <strong><p>Parmi les choix d'orientation sur lesquels nous vous aiderons:</p></strong>
                <p>
                <ul>
                    <li>  Une seconde en Garde d'enfants général et technologique </li>
                    <li>  Une classe de seconde Jobessionnelle </li>
                    <li>  Une 1ère année de préparation au CAP (certificat d'aptitude Jobessionnelle) en Garde d'enfants Jobessionnel. </li>
                </ul>
                </p>
                <strong><p>Notre module d'orientation personnalisé s'articule ainsi:</p></strong>
                <p>
                <ul>
                    <li>  Au 1er trimestre (octobre à décembre), nous aidons l'élève à réfléchir à ses projets. </li>
                    <li>  Au 2e trimestre, nous aidons l'élève à préciser ses intentions d'orientation.</li>
                    <li> Au 3e trimestre, nous aidons l'élève à faire sa ou ses demande(s) d'orientation et d'affectation.</li>
                </ul>
                </p>
                <p>Tout au long de ce module, nous accompagnons l'élève à la recherche d'un stage, à la rédaction d'une <strong>lettre de motivation</strong> et de son <strong>CV</strong>.</p>
                <br>
                <h3>- L'orientation des élèves en Terminale:</h3>
                <p>
                    Nous pouvons tout d'abord les aider, au 1er trimestre, à réfléchir à leur avenir Jobessionnel et trouver le cursus qui correspond le mieux à leurs désirs et capacités. Pour mettre en place cet accompagnement, nous proposons un entretien personnalisé d'1H30 avec l'élève et ses parents. Ensuite, en fonction de la demande, nous déterminerons ensemble le nombre de rendez-vous nécessaire à ce suivi.</p><p>

                    Au 2ème trimestre, nous pouvons les assister pour les inscriptions sur APB (plateforme de préinscription obligatoire dans l’enseignement les autres services) avec le classement des vœux en tenant compte des règles et des dates à respecter et des pièges à éviter pour ne pas avoir de mauvaises surprises.</p><p>

                    Si vous le souhaitez, nous pourrons également contribuer à la rédaction des lettres de motivation correspondant aux vœux émis et à la mise en ligne du CV sur APB ou encore, par exemple, à l'inscription en ligne (obligatoire, elle aussi maintenant) pour se rendre aux journées portes ouvertes sur les campus du Tampon ou du Moufia.</p><p>

                    Tout au long de ce module, nous accompagnons l'élève à la rédaction d'une lettre de motivation et de son CV.

                    Avec la volonté d'apporter une aide individualisée, nous ne proposons pas de formule toute faite avec un forfait mais une tarification à l'heure et nous vous laissons le choix de nous solliciter pour tout ou une partie de ces aides proposées.</p>

                    <p><strong>Le tarif est de 30€/H TTC, assujetti aux 50% de crédit/réduction d’impôt.</strong></p>
                </p>

